public class switch_break_st {
    public static void main(String[] args) {
        int i=1;
        switch (i){
            case 1:
                System.out.println("Value is one");
                break;
            case 2:
                System.out.println("Value is two");
                break;
            default:
                System.out.println("No Value");
        }
    }
}
